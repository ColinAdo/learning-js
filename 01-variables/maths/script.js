let x;

x = Math;

console.log(x);

x = Math.min(1, 4, 6);
console.log(x);

x = Math.max(2, 5, 0);
console.log(x);

x = Math.pow(2, 2);
console.log(x);

x = Math.sqrt(4);
console.log(x);

x = Math.ceil(5.2); // Always rounds up
console.log(x);

x = Math.round(5.2); // Rounds up based on number after the decimal place.
console.log(x);

x = Math.floor(5.9); // Rounds down 
console.log(x);

x = Math.random();
console.log(x);

x = Math.random() * 10 + 1;  // Produces number btn 0 to 10 in decimal places
console.log(x);

x = Math.floor(Math.random() * 10 + 1);  // Produces number btn 0 to 10 in decimal places
console.log(x);