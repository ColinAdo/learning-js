// Defining a function
function greetings() {
    console.log("Hello, world!");
}

greetings(); // Calling a function

function sum(a, b) { // a, b are called parameters
    console.log(a + b);
}
sum(2, 3); // 2, 3 are called arguments

function sub(x, y) {
    return x - y;
}
const ans = sub(14, 1);
console.log(ans);
